def upper_string(val):
    """ Функция переводит текст в верхний регистр
    тест"""
    val = input(str)
    return val.upper()


print(upper_string(""))


def cap_string(symbol):
    """переводит первый символы в верхний регистр
    тест финал
    """
    symbol = input(str)
    return symbol.capitalize()


print(cap_string(""))
